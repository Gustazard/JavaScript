const resultadoADICAO = (contaADICAO);

const contaSUBTRACAO = 400 - 252;
const resultadoSUBTRACAO = (contaSUBTRACAO);

const contaDIVICAO = 780 / 12;
const resultadoDIVICAO = (contaDIVICAO);

const contaMULTIPLICACAO = 1024 * 64;
const resultadoMUTIPLICACAO = (contaMULTIPLICACAO);

const resultadoFINAL = (`${resultadoADICAO} \n${resultadoSUBTRACAO} \n${resultadoDIVICAO} \n${contaMULTIPLICACAO}`)


console.log (resultadoFINAL);

